# Выполнить логические побитовые операции «И»,
# «ИЛИ» и др. над числами 5 и 6.
# Выполнить над числом 5 побитовый сдвиг
# вправо и влево на два знака.

a = 5
b = 6
print("a & b =", a & b)
print("a | b =", a | b)
print("~a =", ~ a)
print("a ^ b =", a ^ b)
print("a >> 2 =", a >> 2)
print("b >> 2 =", b >> 2)
print("a << 2 =", a << 2)
print("b << 2 =", b << 2)

